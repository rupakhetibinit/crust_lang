#[derive(Debug, Clone, PartialEq, Eq)]
pub enum Token {
    Let,
    Ident(String),
    RawString(String),
    Number(i64),
    Plus,
    Minus,
    Star,
    Slash,
    Equal,
    Semicolon,
    LParen,
    RParen,
    EOF,
    Caret,
    LBrace,
    RBrace,
    Print,
    Fn,
    Comma,
    Return,
    ElseIf,
    If,
    Else,
    EqualEqual,
    For,
    Increment,
    LessThan,
    Less,
    GreaterThan,
    Greater,
    And,
    Or,
    Modulo,

    // Struct-related tokens
    StructDecl,
    StructImpl,

    LBracket,
    RBracket,
    Colon,
    Dot,
    DoubleColon,
}
